#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	i;
	char	*dup;

	dup = NULL;
	i = 0;
	while (src[i])
		i++;
	dup = (char *) malloc(i + 1 * sizeof(char));
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
