#include "../so_long.h"

void	map_formating(char ***map, size_t i, size_t y)
{
	if (map[i][1][y] == '0')
	{
		map[i][1][y] = '1';
		map[i][0][y] = '1';
	}
	if (i > 1 && y > 1 && map[i][1][y] == '1' && map[i + 1] \
		&& map[i - 1][1][y] == '1' && map[i - 1][1][y - 1] == '1' \
		&& map[i][1][y - 1] == '1' && map[i][1][y + 1] != '\n')
	{
		map[i][1][y] = '2';
		map[i - 1][1][y] = '2';
		map[i - 1][1][y - 1] = '2';
		map[i][1][y - 1] = '2';
	}
}
